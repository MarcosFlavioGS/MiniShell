#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		s_size;
	int		i;
	char	*dup;

	i = 0;
	s_size = ft_strlen(s);
	dup = (char *)malloc((s_size + 1) * sizeof(char));
	if (!dup)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
